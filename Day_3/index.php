<br>No, no, and sorry, but no. You can purchase an electronic copy of the book, however: Amazon.com now sells a version of <em> Introduction to Algorithms</em>
for Kindle.<br><br><em>I see that there are two versions of the second edition, one published by the MIT Press and one published by McGraw-Hill. How do the two versions differ?</em>    
<br>Other than minor differences in the covers, the book content in the two versions is identical. McGraw-Hill also includes a CD (see the next question). <br><br>
<em>Are the algorithms in the book implemented in a real programming language, rather than just pseudocode?</em>
<br>In the second edition, McGraw-Hill included with the book a CD containing Java implementations of all the algorithms in Parts I-VI. The CD also has Javadoc-generated web pages that <br>
document all the classes. We did not update the Java implementations for the third edition. <br><br>
For the fourth edition, we have made available on the MIT Press website <a href="#">implementations in Python</a> by Linda Xiao and me. 
